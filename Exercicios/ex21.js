//VARIÁVEIS COMPOSTAS

var num = [1, 2, 3]; //Ordem dentro dos colchetes: 0 - 1 - 2

console.log(num); //Mostra todos os elementos da variável
console.log(num.length); //.length Mostra quantas elementos tem variável
console.log(num[1]); //Mostra apenas o elemento que esta na "casa" 1, nesse caso, o número 2

console.log(num.indexOf(3)); //Busca dentro da variável se o valor digitado (3), existe e qual a posição dele
console.log(num.indexOf(4)); //Caso o valor não exista, ele retorna -1
